public class bonhomolde {
    int IdBN;
    String BoNho;

    public bonhomolde() {
    }

    public bonhomolde(int IdBN, String BoNho) {
        this.IdBN = IdBN;
        this.BoNho = BoNho;
    }

    public int getIdBN() {
        return IdBN;
    }

    public void setIdBN(int IdBN) {
        this.IdBN = IdBN;
    }

    public String getBoNho() {
        return BoNho;
    }

    public void setBoNho(String BoNho) {
        this.BoNho = BoNho;
    }
    
}
