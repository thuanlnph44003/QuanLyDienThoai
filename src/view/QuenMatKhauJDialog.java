    private void doiMatKhau() {
        String email = txtEmail.getText();
        String matKhauCu = new String(txtPassOld.getPassword());
        String matKhauMoi = new String(txtPassNew1.getPassword());
        String xacNhanMatKhauMoi = new String(txtPassNew2.getPassword());

        if (email.isEmpty() && matKhauCu.isEmpty() && matKhauMoi.isEmpty() && xacNhanMatKhauMoi.isEmpty()) {
            HienThiND.ND(this, "Vui lòng nhập thông tin.");
            return;
        }
        if (email.isEmpty()) {
            HienThiND.ND(this, "Vui lòng nhập mail đăng nhập");
            return;
        }
        if (matKhauCu.isEmpty()) {
            HienThiND.ND(this, "Vui lòng nhập mật khẩu cũ");
            return;
        }
        if (matKhauMoi.isEmpty()) {
            HienThiND.ND(this, "Vui lòng nhập mật khẩu mới");
            return;
        }

        if (matKhauMoi.isEmpty() || xacNhanMatKhauMoi.isEmpty()) {
            HienThiND.ND(this, "Vui lòng nhập lại mật khẩu mới");
            return;
        }

        if (!matKhauMoi.equals(xacNhanMatKhauMoi)) {
            HienThiND.ND(this, "Mật khẩu xác nhận không đúng!");
            return;
        }

        NhanVien nv = dao.selectByEmail(email);
        if (nv == null) {
            HienThiND.ND(this, "Tên đăng nhập không tồn tại!");
            return;
        }

        if (!nv.getMatKhau().equals(matKhauCu)) {
            HienThiND.ND(this, "Sai mật khẩu cũ!");
            clearForm();
            return;
        }

        nv.setMatKhau(matKhauMoi);
        dao.updateMKMail(nv);

        HienThiND.ND(this, "Đổi mật khẩu thành công!\nVui lòng đăng nhập lại");
        dispose();
        TrangChu trangChu = new TrangChu();
        trangChu.setVisible(true);
        //dispose();
    }

