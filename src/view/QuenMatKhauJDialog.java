    private boolean checkmail() {
        String Email = "^[\\w-_.+]*[\\w-_.]@(gmail\\.com|fpt\\.edu\\.vn)$";

        String mail = txtEmail.getText().trim();
        if (txtEmail.getText().isEmpty()) {
            HienThiND.ND(this, "Chưa nhập email");
            return true;
        } else if (!mail.matches(Email)) {
            HienThiND.ND(this, "Định dạng email không hợp lệ!");
            return true;
        } else {
            // Kiểm tra xem có tài khoản nào đăng ký bằng email này hay không
            NhanVienService daonv = new NhanVienService();
            if (daonv.selectByEmail(txtEmail.getText()) == null) {
                HienThiND.ND(this, "Không có tài khoản nào đăng ký bằng email này!");
                return true;
            }
        }
        return false;
    }

    private String generateOTP() {
        Random rd = new Random();
        int numbercode = rd.nextInt(9000) + 1000;
        return String.valueOf(numbercode);
    }

    private boolean checkOTP() {
        if (txtMaxacnhan.getText().isEmpty()) {
            HienThiND.ND(this, "Mã xác nhận trống");
            return true;
        }

        // Kiểm tra xem mã OTP có còn hiệu lực không
        if (isOTPExpired()) {
            HienThiND.ND(this, "Mã OTP đã hết hạn. Vui lòng gửi lại mã mới.");
            return true;
        }

        Pattern regex = Pattern.compile("[a-zA-Z]");
        if (regex.matcher(txtMaxacnhan.getText()).find()) {
            HienThiND.ND(rootPane, "Mã OTP sai định dạng!");
            return true;
        }

        return false;
    }

    private void confirmCode() {
        if (txtMaxacnhan.getText().equals(OTP)) {
            HienThiND.ND(this, "Mã OTP chính xác!\nHãy đổi lại mật khẩu.");
            jLabel4.setVisible(true);
            jLabel5.setVisible(true);
            jLabel6.setVisible(true);
            txtPassOld.setVisible(true);
            txtPassNew1.setVisible(true);
            txtPassNew2.setVisible(true);
            btnDoiMK.setVisible(true);
        } else {
            HienThiND.ND(this, "Mã OTP không chính xác!");
            txtMaxacnhan.requestFocus();
        }
    }

    private boolean isOTPExpired() {
        // Lấy thời điểm hiện tại
        long currentTime = System.currentTimeMillis();
        // Kiểm tra xem đã qua 3 phút từ thời điểm bắt đầu gửi mã hay chưa
        return currentTime > (otpStartTime + 3 * 60 * 1000);
    }
