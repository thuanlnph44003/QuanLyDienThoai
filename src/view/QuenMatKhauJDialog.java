    private void btnLayMaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLayMaActionPerformed
        if (checkmail()) {
            return;
        }

        // Nếu email hợp lệ, thực hiện gửi mã OTP
        try {
            btnLayMa.setEnabled(true);
            btnDoiMK.setEnabled(true);
            // Cấu hình thông tin email
            Properties properties = new Properties();
            properties.put("mail.smtp.auth", "true");
            properties.put("mail.smtp.starttls.enable", "true");
            properties.put("mail.smtp.host", "smtp.gmail.com");
            properties.put("mail.smtp.port", 587);

            // Thông tin đăng nhập email
            String username = "quanlybandienthoaimobileshop4@gmail.com"; // Thay bằng địa chỉ email của bạn
            String password = "iubs fhfs tdvj ikxo"; // Thay bằng mật khẩu email của bạn

            // Tạo phiên gửi email
            Session session = Session.getInstance(properties, new javax.mail.Authenticator() {
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(username, password);
                }
            });

            // Gửi mã OTP
            String from = username;
            to = txtEmail.getText();
            OTP = generateOTP();
            String title = "Quên mật khẩu";

            Message msg = new MimeMessage(session);
            msg.setFrom(new InternetAddress(from));
            msg.setRecipients(Message.RecipientType.TO, InternetAddress.parse(to));
            msg.setSubject(title);
            msg.setText("Mã OTP: " + OTP + " Đây là mã OTP của bạn, sẽ hết hạn trong vòng 3 phút. Vui lòng không để lộ thông tin này!");

            // Sử dụng Transport.send() để gửi
            Transport.send(msg);

            // Lưu thời điểm khi gửi mã OTP để kiểm tra hết hạn
            otpStartTime = System.currentTimeMillis();

            HienThiND.ND(this, "Đã gửi mã OTP! Hãy kiểm tra email của bạn.");
            txtMaxacnhan.setEnabled(true);
        } catch (AuthenticationFailedException e) {
            e.printStackTrace();
            HienThiND.ND(this, "Lỗi xác thực. Vui lòng kiểm tra lại thông tin đăng nhập.");
        } catch (MessagingException e) {
            e.printStackTrace();
            HienThiND.ND(this, "Đã xảy ra lỗi khi gửi mã OTP. Vui lòng thử lại sau.");
        }
    }//GEN-LAST:event_btnLayMaActionPerformed

