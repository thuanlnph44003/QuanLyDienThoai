    void loadData() {
        data.setRowCount(0);
        dsTK = service.findAll();
        for (ThongKemodel thongKe : dsTK) {
            data.addRow(new Object[]{
                thongKe.getID_MaHoaDon(),
                thongKe.getID_MaHoaDonChiTiet(),
                thongKe.getTenSanPham(),
                thongKe.getSoLuong(),
                thongKe.getGiaBan()
            });

        }
    }

    void fillComboBoxes() {

        List<ThongKemodel> listThongKe = service.findTong();
        for (ThongKemodel nv : listThongKe) {
            txtTTDoanhThu.setText(String.valueOf(nv.getTongDoanhThu()));
        }

    }

    void fillDOnHangDT() {
        List<ThongKemodel> listDH = service.findDHDT();
        for (ThongKemodel dh : listDH) {
            txtTongDonHangDoanhThu.setText(String.valueOf(dh.getTongDonHang()));
        }
    }

