void fillthang() {
    // Lấy tháng đã chọn từ ComboBox
    String selectedMonth = (String) cboLocThang.getSelectedItem();
    // Chuyển đổi giá trị tháng từ String sang int
    int month = Integer.parseInt(selectedMonth);
    // Lấy mô hình bảng hiện tại từ bảng của bạn
    DefaultTableModel molde2 = (DefaultTableModel) tblThongKe.getModel(); // yourTableName là tên của bảng của bạn
    // Xóa dữ liệu cũ trong mô hình bảng
    molde2.setRowCount(0);
    // Lấy dữ liệu từ Service dựa trên tháng đã chọn
    dsTKk = service.findAllByMonth(month);
    // Điền dữ liệu vào bảng từ danh sách dữ liệu theo tháng
    for (ThongKemodel thongKe : dsTKk) {
        molde2.addRow(new Object[]{
            thongKe.getID_MaHoaDon(),
            thongKe.getID_MaHoaDonChiTiet(),
            thongKe.getTenSanPham(),
            thongKe.getSoLuong(),
            thongKe.getGiaBan()
        });
    }
}

